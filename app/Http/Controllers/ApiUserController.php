    public function usersList($page) {
        $limit = 20;
        $from = ($page -1) * $limit;

        return User::skip($from)
            ->take($limit)->get();
    }


    public function login(Request $request) {
        $email = $request->get('email');
        $password = $request->get('password');
        if(Auth::attempt([
            'password' => $password,
            'email' => $email,
        ])) {
            return User::where('email', $email)->first();
        }
        return "";
    }
